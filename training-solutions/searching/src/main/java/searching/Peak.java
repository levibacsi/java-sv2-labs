package searching;

public class Peak {
    private String peakName;
    private int peakHeight;

    public Peak(String peakName, int peakHeight) {
        this.peakName = peakName;
        this.peakHeight = peakHeight;
    }

    public String getPeakName() {
        return peakName;
    }

    public int getPeakHeight() {
        return peakHeight;
    }
}
