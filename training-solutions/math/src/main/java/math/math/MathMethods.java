package math.math;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(23, 35));
        System.out.println(Math.min(23, 35));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(2.75673));
        System.out.println(Math.abs(-5.345634));
        System.out.println(Math.negateExact(8));
        System.out.println(Math.addExact(4, 5));
        System.out.println(Math.subtractExact(4, 5));
        System.out.println(Math.multiplyExact(9, 6));
        System.out.println(Math.pow(2.1, 3.4));
        System.out.println(Math.incrementExact(0));
        System.out.println(Math.decrementExact(1));
        System.out.println(Math.random());
    }
}
